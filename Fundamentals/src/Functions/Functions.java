package Functions;

public class Functions {
    // Um método é um bloco de código que pode ser REUTILIZADO

    /*
    Declaração:
        - Method Header:
            I. Access Specifier: public, protected, private, default
            II. Return type: void, primitive types, non-primitive types
            III. Method name: nome do método
            IV. Parameter List: (int numero, String nome)
        - Method Body: Bloco de códigos que resolve o problema
        - Return: Caso o método não seja void ele deve retornar de acordo com o tipo declarado

        Boas práticas:
            O nome do método deve ser um verbo
     */

    public int somar(int a, int b){
        return a + b;
    }
}
